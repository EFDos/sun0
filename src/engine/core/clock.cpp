#include "clock.hpp"

namespace sun {

clock::clock()
{
    reset();
}

void clock::reset()
{
    start_ = high_res_clock::now();
}

time clock::elapsed()
{
    return time(std::chrono::duration_cast<milliseconds>
                (high_res_clock::now() - start_).count());
}

}
